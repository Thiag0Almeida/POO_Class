package Contagem;
public class Contagem {
    public static void main(String[] args) {
        System.out.println("Contagem:");
        for(int i = 0; i < 5; i++){
            System.out.println(i);
        }

        System.out.println("----------------------");

        System.out.println("Regressiva:");
        for(int i = 4; i >=0; i--){
            System.out.println(i);
        }
    }
}
